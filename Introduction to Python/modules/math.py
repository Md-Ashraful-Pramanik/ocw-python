import math

print(math.radians(30))
print(math.pi / 6)
print(math.sin(math.radians(30)))

print(math.sqrt(49))
print(math.sqrt(50))
print(math.isqrt(50))
print(math.sqrt(48))
print(math.isqrt(48))

# floor
# 4.45646 => 4, 5.9999 => 5
print(math.floor(5.9999))
print(math.floor(4.4654))

# ceil
print(math.ceil(5.9999))
print(math.ceil(4.4654))

# round
# 5.5 => 6, 5.4 => 5 
print(round(5.5))
print(round(5.4))

